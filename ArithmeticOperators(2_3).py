# Example code shows different arithmetic operatios

x = 15
y = 4

# Output: x + y = 19
print('x + y =', x+y)

# Output: x - y = 11
print('x - y =', x-y)

# Output: x multiplied by y = 60
print('x * y =', x*y)

# Output: x / y = 3.75
print('x / y =', x/y)

# Output: x // y = 3  (returns the integer part of the answer)
print('x // y =', x//y)

# Output: x % y = 3  (returns the remainder of the division)
print('x % y =', x%y)

# Output: x ** y = 50625  (returns x ^ y)
print('x ** y =', x**y)